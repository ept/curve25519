void printkey(unsigned char *buf)
{
  for (int i = 0;i < 32;++i) {
    if (i > 0) printf(","); else printf(" ");
    printf("0x%02x",(unsigned int) buf[i]);
    if (i % 8 == 7) printf("\n");
  }
}

